
public class TiposPrimitivos {

	public static void main(String[] args) {
		
		Byte variavelByte = 1; // tipo primitivo é byte
		 
		Short variavelShort = 1; // tipo primitivo é short
		
		Integer variavelInteger = 1; // tipo primitivo é int
		
		Long variavelLong = 1L; // tipo primitivo é long
		
		Float variavelFloat = 1.0f; // tipo primitivo é float
		
		Double variavelDouble = 1.0; // tipo primitivo é double

		Character variavelCharacter = 'A'; // tipo primitivo é char
		
		Boolean variavelBoolean = true; // tipo primitivo é boolean
		
		// String não tem tipo primitivo
		
		String variavelString = "";
		
		
	}
}
