
public class TiposNumericos {

	public static void main(String[] args) {
		
		Byte numeroByte = 1; // -128 até 127
		
		short numeroShort = 1; // -32768 até 32767 
		
		Integer numeroInteger = 1; // 2^31 = 2147483648 até (2147483648 -1) = 2147483647
		
		Long numeroLong = 1l; // 2^63 = -9,223372037x10^18 até 9,223372037x10^18 
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0;
				
	}
}
