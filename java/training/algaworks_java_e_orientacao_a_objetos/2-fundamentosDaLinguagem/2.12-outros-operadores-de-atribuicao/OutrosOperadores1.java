public class OutrosOperadores1 {

	public static void main (String[] args) {
		
		int valor = 10;
		
		System.out.println("Vc pode atribuir valor: \" int valor = valor + 1;\" ");
		System.out.println("Vc pode atribuir valor: \" int valor += 1;\" ");
		System.out.println("Abaixo seguem algumas atribuicoes");

		valor += 3; // valor igual a 13
		System.out.println(valor);

		valor -= 4; // valor igual a 9
		System.out.println(valor);

		valor *= 2; // valor igual a 18
		System.out.println(valor);

		valor /= 2; // valor igual a 9
		System.out.println(valor);

		valor %= 2; // valor igual a 1
		System.out.println(valor);

	}
}
