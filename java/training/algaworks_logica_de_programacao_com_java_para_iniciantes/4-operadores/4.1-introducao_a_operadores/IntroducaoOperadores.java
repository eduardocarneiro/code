
public class IntroducaoOperadores {

	public static void main(String[] args) {
		
		System.out.println("[Operador]    [Tipo]           [Descrição]");
		System.out.println("+ e -         ARITMÉTICO       Adição e subtração");
		System.out.println("* e /         ARITMÉTICO       Multiplicação e divisão");
		System.out.println("%             ARITMÉTICO       Módulo");
		System.out.println("> e >=        RELACIONAL       Maior que e maior ou igual a");
		System.out.println("< e <=        RELACIONAL       Menor que e menor ou igual a");
		System.out.println("== e !=       RELACIONAL       Igual e diferente");
		System.out.println("=             ATRIBUIÇÃO       Atribui algum valor para a variável");
		System.out.println("+= e -=       ATRIBUIÇÃO       Faz a operação - adição ou subtração - e, depois, a atribuição");
		System.out.println("*= e /=       ATRIBUIÇÃO       Faz a operação - multiplicação ou divisão - e, depois, a atribuição");
		System.out.println("%=            ATRIBUIÇÃO       Faz a operação - módulo - e, depois, a atribuição");
		System.out.println("&&            LÓGICO           Os dois lados do operador devem ser verdadeiros");
		System.out.println("||            LÓGICO           Um dos lados deve ser verdadeiro");
		System.out.println("!             LÓGICO           Reverte o valor");
	}
}
